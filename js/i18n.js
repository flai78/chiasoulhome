/* ── Engine ─────────────────────────────────────────────────── */
function applyLang(lang) {
  const t = TRANSLATIONS[lang] || TRANSLATIONS.en;

  // Plain text
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // HTML content (elements with <strong>, <br>, etc.)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.dataset.i18nPh;
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // Active button highlight
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  document.documentElement.lang = lang;
  localStorage.setItem('chia-lang', lang);
}

function detectLang() {
  const supported = Object.keys(TRANSLATIONS); // ['en','it','es','de','fr']
  // 1. User previously picked a language — honour it
  const saved = localStorage.getItem('chia-lang');
  if (saved && supported.includes(saved)) return saved;
  // 2. Walk browser preference list (full ISO codes e.g. "it-IT", "fr-FR")
  //    and pick the first match on the primary language subtag
  const prefs = navigator.languages && navigator.languages.length
    ? navigator.languages
    : [navigator.language || 'it'];
  for (const pref of prefs) {
    const code = pref.split('-')[0].toLowerCase();
    if (supported.includes(code)) return code;
  }
  return 'it';
}

document.addEventListener('DOMContentLoaded', () => {
  applyLang(detectLang());

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => applyLang(btn.dataset.lang));
  });

  // Mobile nav toggle
  const toggle = document.querySelector('.nav-toggle');
  const navLinks = document.querySelector('.nav-links');
  if (toggle && navLinks) {
    toggle.addEventListener('click', () => {
      const open = navLinks.classList.toggle('open');
      toggle.classList.toggle('open', open);
      toggle.setAttribute('aria-expanded', open);
    });

    // Close menu when any nav link or lang button is tapped
    const closeMenu = () => {
      navLinks.classList.remove('open');
      toggle.classList.remove('open');
      toggle.setAttribute('aria-expanded', 'false');
    };
    navLinks.querySelectorAll('a').forEach(a => a.addEventListener('click', closeMenu));
    navLinks.querySelectorAll('.lang-btn').forEach(btn => btn.addEventListener('click', closeMenu));
  }
});
